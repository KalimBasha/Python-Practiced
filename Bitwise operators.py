#Bitwise operators change the no to binary code and checks the condition
#And
print(26&31)
print(73&88)

#Or
print(54|61)
print(26|31)

#Xor
print(54^61)
print(420^123)

#Tilde
print(~20)
print(~(-3))
print(~-87)

#Left Shift
print(54<<3)
print(14<<2)
print(19<<3)
print(67<<4)

#Right Shift
print(19>>2)
print(14>>1)
print(67>>4)
